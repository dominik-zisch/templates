#!/usr/bin/python

import sys
import signal
import time


# ============================================================================//
# --------------------------------------------------------------------// Globals

running = True

# --------------------------------------------------------------------// Globals
# ----------------------------------------------------------------------------//



# ============================================================================//
# -------------------------------------------------------------// Signal handler

# -----------------------------------------------/
# ---/ catch Ctrl+C to end program
def signal_handler( signal, frame ):
    global running
    running = False

# -------------------------------------------------------------// Signal handler
# ----------------------------------------------------------------------------//



# ============================================================================//
# ---------------------------------------------------------// Main program logic

def main():
    global running

    # register signal handler for Ctrl+C
    signal.signal( signal.SIGINT, signal_handler )

    # init stuff
    pass

    # main program loop
    while ( running ):
        time.sleep( 0.1 )

    # cleanup
    print( 'Closing program!' )
    sys.exit( 0 )


if __name__ == '__main__':
    main()

# ---------------------------------------------------------// Main program logic
